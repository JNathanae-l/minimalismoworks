

function hiddeComponentsVision() {

    var componentsVision = document.getElementById("content-principal-vision");
    var componentsVisionText = document.getElementById("allVision");
    componentsVision.style.display='none'
    componentsVisionText.style.display='flex'
}

function showComponentsVision() {

    var componentsVision = document.getElementById("content-principal-vision");
    componentsVision.style.display='flex';
    var componentsVisionText = document.getElementById("allVision");
    componentsVisionText.style.display='none'
}

